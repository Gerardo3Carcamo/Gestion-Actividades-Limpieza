    private void setValues(){
        this.TareaAsignacion.removeAllItems();
        this.ColoniaCreacion.removeAllItems();
        this.EncargadoAsignacion.removeAllItems();
        map.forEach((x, y) ->{
            this.TareaAsignacion.addItem(x);
        });
        listColonia.forEach((x,y) ->{
            this.ColoniaCreacion.addItem(x);
        });
        mapEncargados.forEach((x, y) -> {
            this.EncargadoAsignacion.addItem(x);
        });
        
    }

