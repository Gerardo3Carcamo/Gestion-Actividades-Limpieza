    private void GuardarCreacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarCreacionActionPerformed
        Object[] data = new Object[]{
            Descripcion.getText(),
            (Date)new Date(),
            "Creada",
            (int)listColonia.get(ColoniaCreacion.getSelectedItem().toString())
        };
        new TareaController().insertTareas(data);
        map = new TareaController().getTareasMap();
        listColonia = new ColoniaController().getColoniasMap();
        setValues();
        new CustomDialog(this, true, "OK", "Tarea creada correctamente", 1).setVisible(true);
        this.Descripcion.setText("");
    }//GEN-LAST:event_GuardarCreacionActionPerformed

    private void GuardarAsignacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarAsignacionActionPerformed
        Object[] data = new Object[]{
            (int)map.get(TareaAsignacion.getSelectedItem().toString()),
            (int)mapEncargados.get(EncargadoAsignacion.getSelectedItem().toString()),
            (Date) new Date()
        };
        new TareaController().insertAsignacionTareas(user.getUserID(), data);
        new CustomDialog(this, true, "OK", "Tarea asignada correctamente", 1).setVisible(true);
    }//GEN-LAST:event_GuardarAsignacionActionPerformed

